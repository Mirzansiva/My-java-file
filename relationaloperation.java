class relationaloperators{
	public static void main(String args[]){
		int x,y;
		x = 20;
		y = 35;
		
		System.out.println("x == y :"+(x == y));
		System.out.println("x != y :"+(x != y));
		System.out.println("x <= y :"+(x <= y));
		System.out.println("x >= y :"+ (x >= y ));
		System.out.println("x > y :"+ (x > y ));
		System.out.println("x < y :"+ (x < y ));
	}
}
		