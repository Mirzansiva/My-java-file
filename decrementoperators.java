class decrement{
	public static void main(String args[]){
		int x = 15;
		System.out.println(x);
		
		x = x-5;
		System.out.println(x);
		
		x = 5;
		System.out.println(x);
		
		x--;
		System.out.println(x);
		
		--x;
		System.out.println(x);
		
		System.out.println(--x);
		System.out.println(x--);
		System.out.println(x);
	}
}